// Fetches WC-2026 teams + fixtures from football-data.org and emits an idempotent
// seed: maps teams.external_id by name (with overrides), then inserts 104 fixtures.
// Run: FOOTBALL_DATA_TOKEN=xxx node scripts/generate-matches-seed.mjs
import { writeFile } from "node:fs/promises";

const TOKEN = process.env.FOOTBALL_DATA_TOKEN;
if (!TOKEN) throw new Error("set FOOTBALL_DATA_TOKEN");
const BASE = "https://api.football-data.org/v4/competitions/WC";
const OUT = "supabase/seed/0012_seed_matches.sql";

// football-data team name -> our teams.name, for the 5 known diffs (else identical).
const NAME_OVERRIDE = {
  "Bosnia-Herzegovina": "Bosnia & Herzegovina",
  "Cape Verde Islands": "Cape Verde",
  "Czechia": "Czech Republic",
  "Congo DR": "DR Congo",
  "United States": "USA",
};
const STAGE = {
  GROUP_STAGE: "group", LAST_32: "r32", LAST_16: "r16",
  QUARTER_FINALS: "qf", SEMI_FINALS: "sf", THIRD_PLACE: "third_place", FINAL: "final",
};
const esc = (s) => s.replace(/'/g, "''");
const get = async (path) => {
  const r = await fetch(BASE + path, { headers: { "X-Auth-Token": TOKEN } });
  if (!r.ok) throw new Error(`${path} -> ${r.status}`);
  return r.json();
};

const teamsRes = await get("/teams");
const ourName = (fd) => NAME_OVERRIDE[fd] ?? fd;
const teamLines = teamsRes.teams
  .map((t) => `update teams set external_id = '${t.id}' where name = '${esc(ourName(t.name))}';`)
  .sort();
if (teamLines.length !== 48) throw new Error(`expected 48 teams, got ${teamLines.length}`);

const matchesRes = await get("/matches");
const ms = matchesRes.matches;
if (ms.length !== 104) throw new Error(`expected 104 matches, got ${ms.length}`);
const teamRef = (id) => (id == null ? "null" : `(select id from teams where external_id = '${id}')`);
const matchRows = ms.map((m) => {
  const stage = STAGE[m.stage];
  if (!stage) throw new Error(`unknown stage ${m.stage}`);
  const grp = m.group ? `'${m.group.replace(/^GROUP_/, "")}'` : "null";
  return `  ('${m.id}', '${stage}', ${grp}, ${teamRef(m.homeTeam?.id)}, ${teamRef(m.awayTeam?.id)}, '${m.utcDate}', 'scheduled')`;
});

const sql = `-- GENERATED by scripts/generate-matches-seed.mjs — do not edit by hand.
-- 1) Map our teams to football-data ids (idempotent).
${teamLines.join("\n")}

-- 2) Seed the 104 fixtures. Knockout fixtures have null teams until rounds resolve.
insert into matches (external_id, stage, group_letter, home_team_id, away_team_id, kickoff_at, status) values
${matchRows.join(",\n")}
on conflict (external_id) do update set
  stage = excluded.stage,
  group_letter = excluded.group_letter,
  home_team_id = coalesce(excluded.home_team_id, matches.home_team_id),
  away_team_id = coalesce(excluded.away_team_id, matches.away_team_id),
  kickoff_at = excluded.kickoff_at;
`;
await writeFile(OUT, sql);
console.log(`Wrote ${teamLines.length} team maps + ${matchRows.length} fixtures to ${OUT}`);
